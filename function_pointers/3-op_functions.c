#include "3-calc.h"

/**
*op_add - the sum
*@a:the first number
*@b:the second number
*
*Return:return
*/

int op_add(int a, int b)
{
return (a + b);
}


/**
*op_sub - the difference of two numbers
*@a:The first number
*@b:The second number
*
*Return: return
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
*op_mul - Returns the multiplication of two numbers
*@a:The first number
*@b: The second number
*
*Return: The result
*/
int op_mul(int a, int b)
{
return (a * b);
}


/**
*op_div - Returns the division of two numbers.
*@a: The first number.
*@b: The second number.
*
*Return: The result
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
*op_mod - Returns the remainder of the division of two numbers.
*@a: The first number.
*@b: The second number.
*
*Return: The result
*/
int op_mod(int a, int b)
{
return (a % b);
}